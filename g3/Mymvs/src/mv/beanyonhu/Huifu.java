package mv.beanyonhu;

public class Huifu {

    private int id;
    private String hf;

    public Huifu() {
    }

    public Huifu(int id, String hf) {
        this.id = id;
        this.hf = hf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getHf() {
        return hf;
    }

    public void setHf(String hf) {
        this.hf = hf;
    }
}
